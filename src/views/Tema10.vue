      h2 10.1 Niveles de segmentación de mercados 

    p.mb-5 Los consumidores demandan bienes y servicios con diferentes características dependiendo del tipo de producto o servicio, los mercados se encuentran ya agrupados o segmentados de acuerdo con las preferencias, gustos y necesidades. El proceso de segmentación se puede realizar según las características del mercado, donde se observan grandes grupos de consumidores con necesidades generalizadas, las organizaciones aplican de acuerdo con estas características los procesos de segmentación de masas. Según Monferrer (2013), “la empresa plantea una única oferta para todos los clientes” (p. 58). 

